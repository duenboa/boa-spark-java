package com.boa.java8;

/**
 * @author DuenBoa
 * @date 2018/2/4
 */
public class Dash {
    private String name;
    private int weight;

    public Dash(String name, int weight) {
        this.name = name;
        this.weight = weight;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getWeight() {
        return weight;
    }

    public void setWeight(int weight) {
        this.weight = weight;
    }
}
